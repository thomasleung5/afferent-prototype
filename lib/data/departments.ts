export function normalizeDeptName(value: string): DeptCode | null {
  const upper = value.trim().toUpperCase();
  if ((FEE_DEPTS as readonly string[]).includes(upper)) return upper as DeptCode;
  const compact = upper
    .replace(/&/g, "AND")
    .replace(/['’]/g, "")
    .replace(/[^A-Z0-9]+/g, " ")
    .trim()
    .replace(/\s+/g, " ");
  return DEPT_ALIASES[compact] ?? null;
}
